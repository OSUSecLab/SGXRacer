int main(int argc, const char* argv[])
{
    oe_result_t result;
    oe_enclave_t* enclave = NULL;

    /* Check command-line argument count */
    if (argc != 2)
    {
        fprintf(stderr, "Usage: %s ENCLAVE_PATH\n", argv[0]);
        return 1;
    }

    /* Create the enclave */
    const uint32_t flags = oe_get_create_flags();
    OE_TEST(
        (result = oe_create_hexdump_enclave(
             argv[1], OE_ENCLAVE_TYPE_AUTO, flags, NULL, 0, &enclave)) ==
        OE_OK);

    /* Test enclave version of oe_hex_string() */
    {
        char hexstr[sizeof(_hexstr)];
        int return_value;

        OE_TEST(
            (result = test(
                 enclave,
                 &return_value,
                 _data,
                 sizeof(_data),
                 hexstr,
                 sizeof(hexstr))) == OE_OK);
        OE_TEST(return_value == 0);
        OE_TEST(strcmp(hexstr, _hexstr) == 0);
    }

    /* Test host version of oe_hex_string() */
    {
        char hexstr[sizeof(_hexstr)];

        OE_TEST(oe_hex_string(hexstr, sizeof(hexstr), _data, sizeof(_data)));
        OE_TEST(strcmp(hexstr, _hexstr) == 0);

        oe_hex_dump(_data, DATA_SIZE);
    }

    /* Terminate the test enclave */
    oe_terminate_enclave(enclave);

    printf("=== passed all tests (hexdump)\n");

    return 0;
}
